fn main() {
    // positive integers only
    let unsigned_num: u8 = 5;

    // positive + negative integers
    let signed_num: i8 = 5;

    // floating point numbers
    let float_num: f32 = 5.0;

    // Characters
    let char: char = 'a';

    // Boolean
    let b: bool = true;

    // Type aliasing
    type Age = u8;
    let peter_age: Age = 42;

    // Type Conversion
    let a: i32 = 10;
    let b: f64 = a as f64;
}
